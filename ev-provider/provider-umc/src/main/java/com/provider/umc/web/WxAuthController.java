    /**
     * 微信登录
     *
     * @param wxLoginInfo 请求内容，{ code: xxx, userInfo: xxx }
     * @param      请求对象
     * @return 登录结果
     */
    @PostMapping("login_by_weixin")
    @ApiOperation(value = "微信登陆",notes = "微信登陆")
    public JsonResult loginByWeixin(@RequestBody WxLoginInfo wxLoginInfo) {
        String code = wxLoginInfo.getCode();
        UserInfo userInfo = wxLoginInfo.getUserInfo();
        if (code == null || userInfo == null) {
            return JsonResult.error("参数错误");
        }

        String sessionKey = null;
        String openId = null;
        try {
            WxMaJscode2SessionResult result = this.wxService.getUserService().getSessionInfo(code);
            sessionKey = result.getSessionKey();
            openId = result.getOpenid();
        } catch (Exception e) {
            e.printStackTrace();
        }

        if (sessionKey == null || openId == null) {
            return JsonResult.error("session失效");
        }

        LitemallUser user = userService.queryByOid(openId);
        if (user == null) {
            user = new LitemallUser();
            user.setUsername(openId);
            user.setPassword(openId);
            user.setWeixinOpenid(openId);
            user.setAvatar(userInfo.getAvatarUrl());
            user.setNickname(userInfo.getNickName());
            user.setGender(userInfo.getGender());
            user.setUserLevel((byte) 0);
            user.setStatus((byte) 0);
            user.setLastLoginTime(LocalDateTime.now());
            user.setLastLoginIp(IpUtil.getIpAddr(request));
            user.setSessionKey(sessionKey);

            userService.add(user);

            // 新用户发送注册优惠券
            couponAssignService.assignForRegister(user.getId());
        } else {
            user.setLastLoginTime(LocalDateTime.now());
            user.setLastLoginIp(IpUtil.getIpAddr(request));
            user.setSessionKey(sessionKey);
            if (userService.updateById(user) == 0) {
                return JsonResult.error("更新失败");
            }
        }

        // token
        String token = UserTokenManager.generateToken(user.getId());
        String thirdSessionKey = RandomStringUtils.randomAlphanumeric(64);
        stringRedisTemplate.opsForValue().set(thirdSessionKey, token, expires, TimeUnit.SECONDS);
        Map<Object, Object> result = new HashMap<Object, Object>();
        result.put("token", thirdSessionKey);
        result.put("userInfo", userInfo);
        return JsonResult.ok(result);
    }


